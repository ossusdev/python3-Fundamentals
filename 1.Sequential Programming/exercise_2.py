###
#Write a program in which four numbers are entered,
# calculate and report the sum of the first two
# and the product of the third and fourth.
###
side_1 = int(input('Load the side 1:'))
side_2 = int(input('Load the side 2:'))
side_3 = int(input('Load the side 3:'))
side_4 = int(input('Load the side 4:'))

sum = side_1 + side_2
product = side_3 * side_4

print('The sum is:'+ str(sum))
print('The product is:'+ str(product))